export const generateDummyProductsResponse = (
  page: number = 1,
  size: number = 12
): ProductsResponse => {
  const startIndex = (page - 1) * size;
  const endIndex = startIndex + size;
  const paginatedProducts = dummyProducts.slice(startIndex, endIndex);

  return {
    data: {
      total: dummyProducts.length,
      totalPage: Math.ceil(dummyProducts.length / size),
      page,
      products: paginatedProducts,
    },
    message: "Products fetched successfully",
  };
};

export const findProductBySlug = (slug: string): Product | undefined => {
  return dummyProducts.find((product) => product.slug === slug);
};
